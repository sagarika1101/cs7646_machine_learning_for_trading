    #read orders.csv into a dataframe and sort by date
    #dfOrders = pd.read_csv(orders_file, index_col='Date', parse_dates=True, na_values=['nan'])
    #dfOrders = dfOrders.sort_values('Date')

    #start_date =  dfOrders.index.values[0] 		
    #end_date = 	 dfOrders.index.values[-1]
    market_dates = pd.date_range(start_date,end_date)

    stocks = list(dfOrders.Symbol.unique())
    stockPrices = get_data(stocks,market_dates)

    stockPrices['portfolioVal'] = 0.0
    stockPrices['transactionCost'] = 0.0
    for i in stocks:
    	symShares = i + 'shares'
    	stockPrices[symShares] = 0.0
    stockPrices['totalCash'] = 100000.0
    stockPrices['isOrderMade'] = 0.0

    totalCash = start_val
    for index,row in dfOrders.iterrows():
    	stock = row['Symbol']
    	orderType = row["Order"]
    	shares = row['Shares']
    	symShares = stock + 'shares'
    	obj = stockPrices.loc[index]
    	transCost = commission + impact*shares*obj[stock]
    	obj['transactionCost'] = transCost
    	Cash = 0.0
    	if orderType=='BUY':
    		obj[symShares] += shares
    		Cash = totalCash - (shares*obj[stock]) - transCost
    		obj['totalCash'] = Cash
    		obj['isOrderMade'] += 1.0
    	else:
    		obj[symShares] -= shares
    		Cash = totalCash + (shares*obj[stock]) - transCost
    		obj['totalCash'] = Cash
    		obj['isOrderMade'] += 1.0
    	totalCash = Cash

    l = len(stocks)
    for i in range(1, stockPrices.shape[0]):
    	for j in range(len(stocks)):
    		stockPrices.iloc[i,(j+l+3)] += stockPrices.iloc[(i-1),(j+l+3)]
    		if stockPrices.iloc[i,-1]==0.0:
    			stockPrices.iloc[i,-2] = stockPrices.iloc[(i-1),-2]

    #update portfolioVal to represent portfolio value of each day
    for i in range(0,stockPrices.shape[0]):
    	portfolio = 0.0
    	#adjust according to number of stocks held that day
    	for j in range(len(stocks)):
    		portfolio += stockPrices.iloc[i,j+1]*stockPrices.iloc[i,(j+l+3)]
    	#add total cash
    	portfolio += stockPrices.iloc[i,-2]
    	#update portfolioVal column
    	#print(portfolio)
    	stockPrices.iloc[i,(l+1)] = portfolio

    portvals = stockPrices.iloc[:,(l+1)]
    portvals = portvals.to_frame()

    return portvals 		   

